package Java_Course.Operators;

public class Bitwise_Operators {
    public static void main(String[] args) {
        // Initial Values
        int a = 5;
        int b = 7;

        // Bitwise and
        // 0101 & 0111=0101 = 5
        System.out.println("a&b = " + (a & b));

        // Bitwise or
        // 0101 | 0111=0101 = 7
        System.out.println("a|b = " + (a | b));
    }
}